## Ingredients

* 2 cups cream or half-and-half
* 3 large eggs
* 2 Tablespoons honey
* 1 Tablespoon vanilla
* 1/2 teaspoon salt
* 8 thick bread slices
* butter

## Directions

Whisk together the cream, eggs, honey, vanilla, and salt until well combined. Cover and refrigerate overnight.

Heat the oven to 375Â° F.

Pour the custard base into a shallow dish. Soak the bread in the custard base, about 20 seconds per side. Place on a wire rack over a rimmed baking sheet and let stand at least 1 minute.

Heat a large skillet over medium heat and add 1 Tablespoon of butter. Fry the soaked bread until well browned, 1 - 2 minutes per side, then remove to the wire rack. Repeat until all the bread is fried.

Place the baking sheet in the oven and bake for 5 minutes. Serve warm with softened butter and maple syrup.
